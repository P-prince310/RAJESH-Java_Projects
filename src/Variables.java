public class Variables {
    public static void main(String[] args) {
        int x; //Declaration
        x = 123; //assignment
        System.out.println(x);

        int y = 124; //initialization
        System.out.println(y);

        int z; //Declaration
        z = 123; //assignment
        System.out.println("my number is : " + z);

        long a = 8384884844888L;//long data type can store -9 quintillion to 9 quintillion
        System.out.println(a);

        byte b = 127; //you can access only 127 but you cant store 128
        System.out.println(b);

        float c = 3.12f;
        System.out.println(c);

        double d = 3.133333;
        System.out.println(d);

        boolean e = true;//boolean only hold true or false value
        System.out.println(e);

        char symbol = '@';/* char only hold character/letter/ASCII value */
        System.out.println(symbol);

        char name = '%';
        System.out.println(name);

        String myname = "rajesh";
        System.out.println("hello my name is : " + myname);
    }
}